package com.tutrit.java.quickstart.service;

public class CalculatorService {

    public int sum(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public int multiplication(int a, int b) {
        return a * b;
    }
}
